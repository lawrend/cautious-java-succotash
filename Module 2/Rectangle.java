
public class Rectangle {
	private int width;
	private int height;
	
	public Rectangle(int newWidth, int newHeight) {
		width = newWidth;
		height = newHeight;
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}
	public void setSize(int newWidth, int newHeight) {
		width = newWidth;
		height = newHeight;
	}
	

}
